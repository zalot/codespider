package com.akanpian.spider;

/**
 * List info
 * @author zhaoheng
 *
 */
public class LInfo {
	public String info;
	public String title;
	public String html;
	public String img;
	public String url;
	public int code;

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getHtml() {
		return html;
	}

	public void setHtml(String html) {
		this.html = html;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}
}
